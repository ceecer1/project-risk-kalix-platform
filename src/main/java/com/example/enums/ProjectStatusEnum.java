package com.example.enums;

/**
 *     NOT_STARTED: The project has not yet begun.
 *     PLANNING: The project is in the planning phase, where initial preparations and requirements gathering are taking place.
 *     IN_PROGRESS: The project is actively being worked on and is in progress.
 *     ON_HOLD: The project has been temporarily halted or put on hold due to various reasons.
 *     COMPLETED: The project has been successfully completed and delivered.
 *     CANCELED: The project has been canceled before completion.
 */
public enum ProjectStatusEnum {
    NOT_STARTED,
    PLANNING,
    IN_PROGRESS,
    ON_HOLD,
    COMPLETED,
    CANCELED
}

